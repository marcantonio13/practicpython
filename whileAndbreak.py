#Ejemplo de Break
print("While con la sentencia break")
contador = 0
while contador < 10:
    contador += 1

    if contador == 5:
        break
    print(" Valor de la varible: ",contador)
print("Fin del contador, la sentencia break se a ejecutado")

#Ejemplo 2 sentencia continuo

print("While con la sentencia continuo \n")
contador = 0
while contador < 10:
    contador += 1

    if contador == 5:
        continue
    print(" Valor de la varible: ",contador)
print("Fin del contador, la sentencia continue se a ejecutado")




