num_dado = int(input("¿Cula es el número que quieres convertir?: "))

if num_dado == 1:
        print("Tu número es Uno")
elif num_dado == 2:
        print("Tu número es Dos")
elif num_dado == 3:
        print("Tu número es Tres")
elif num_dado == 4:
        print("Tu número es Cuatro")
elif num_dado == 5:
        print("Tu número es Cinco")
else :
        print("Este programa solo puede convertir hasta el número 5")

