
#Priemra alternativa
nombre = "Marco"
edad = "33"

print("Hola {} tienes {} a単os.".format(nombre,edad))

#Segunda alternativo

print("Hola {nombre} tienes {edad} a単os.".format(nombre="Marco",edad="32"))

#Tercera alternativa

print("Hola {0} tienes {1} a単os.".format(nombre,edad))

#Cuarta alternativa de sintaxis metodo format

print("Hola {1} tienes {0} a単os.".format(edad,nombre))


